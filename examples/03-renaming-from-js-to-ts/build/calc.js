function sum(number1, number2) {
    return number1 + number2;
}
function sub(number1, number2) {
    return number1 - number2;
}
function mult(number1, number2) {
    return number1 * number2;
}
console.log("**** Math Class from JS****\n    1+1: " + sum(1, 1) + ",\n    2-1: " + sub(2, 1) + ",\n    10*2: " + mult(10, 2));
